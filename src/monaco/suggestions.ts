export const getSuggestions = (range: monaco.Range): monaco.languages.CompletionItem[] => {
  return [
    ...functions.map((f) => ({
      label: f.toLowerCase(),
      insertText: f.toLowerCase(),
      kind: monaco.languages.CompletionItemKind.Function,
      range,
    } as monaco.languages.CompletionItem)),
    ...keywords.map((w) => ({
      label: w.toUpperCase(),
      insertText: w.toUpperCase(),
      kind: monaco.languages.CompletionItemKind.Keyword,
      range,
    })),
    ...types.map((t) => ({
      label: t.toUpperCase(),
      insertText: t.toUpperCase(),
      kind: monaco.languages.CompletionItemKind.Keyword,
      range,
    })),
  ];
};
